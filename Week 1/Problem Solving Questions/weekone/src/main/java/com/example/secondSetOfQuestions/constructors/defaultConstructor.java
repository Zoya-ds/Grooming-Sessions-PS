package com.example.secondSetOfQuestions.constructors;

public class defaultConstructor {
    int num;
    boolean check;

    // default constructor will be created by java

    public static void main(String[] args) {

        defaultConstructor obj1 = new defaultConstructor();
        System.out.println("num: " + obj1.num);
        System.out.println("Boolean: " + obj1.check);
    }

}
